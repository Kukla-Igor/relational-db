CREATE TABLE ORDERS (
ORDER_ID NUMBER,
CONSTRAINT ORDER_PK PRIMARY KEY (ORDER_ID),
CUSTOMER_ID NUMBER,
CONSTRAINT CUSTOMER_FK FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMERS (CUSTOMER_ID),
EMPLOYEE_ID NUMBER,
CONSTRAINT EMPLOYEE_FK FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEES (EMPLOYEE_ID),
SHIPPER_ID NUMBER,
CONSTRAINT SHIPPER_FK FOREIGN KEY (EMPLOYEE_ID) REFERENCES SHIPPERS (SHIPPER_ID),
ORDER_DATE DATE,
REQUIRED_DATE DATE,
SHIPPED_DATE DATE,
SHIP_VIA NUMBER,
FREIGHT NVARCHAR2(30),
SHIP_NAME NVARCHAR2(30),
SHIP_ADDRESS NVARCHAR2(30),
SHIP_CITY NVARCHAR2(30),
SHIP_REGION NVARCHAR2(30),
SHIP_POSTAL_CIDE NUMBER,
SHIP_COUNTRY NVARCHAR2(30)
);