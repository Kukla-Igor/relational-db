CREATE TABLE EMPLOYEES (
EMPLOYEE_ID NUMBER,
CONSTRAINT EMPLOYEE_PK PRIMARY KEY (EMPLOYEE_ID),
LAST_NAME NVARCHAR2(30),
FIRST_NAME NVARCHAR2(30),
TITLE NVARCHAR2(30),
TITLE_OF_COURTESY NVARCHAR2(30),
BIRTH_DAY DATE,
HIRE_DATE DATE,
ADDRESS NVARCHAR2(30),
CITY NVARCHAR2(30),
REGION NVARCHAR2(30),
POSTAL_CODE NUMBER,
COUNTRY NVARCHAR2(30),
HOME_PHONE NUMBER,
EXTENSION NVARCHAR2(30),
PHOTO NVARCHAR2(30),
NOTES NVARCHAR2(30),
REPOTS_TO NVARCHAR2(30)
);